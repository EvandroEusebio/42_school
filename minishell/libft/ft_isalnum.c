#include "libft.h"

int	ft_isalnum(int alphanum)
{
	return ((alphanum >= 65 && alphanum <= 90)
		|| (alphanum >= 97 && alphanum <= 122)
		|| (alphanum >= 48 && alphanum <= 57));
}
